class Qus
{
	public static void main(String [] args)
	{
		//QUSS1
		int x = 8;
		int y = ++x;
		System.out.println(x);
		System.out.println(y);

		//QUS2
		int m = 6;
		int n = ++m;
		System.out.println(m);
		System.out.println(n);

		//QUS3
		int m1 = 15;
		int n1 = --m1;
		System.out.println(m1);
		System.out.println(n1);

		//QUS4
		int z = -3;
		int x1 = --z;
		System.out.println(z);
		System.out.println(x1);

		//Qus5
		int x2 = 7;
		System.out.println((++x2)+(++x2));

		//QUS6
		int y1 = 8;
		System.out.println((--y1)+(--y1)+(--y1));

		//QUS7
		int c = 7;
		int d = 8;
		System.out.println((++c)+(++d)+(++c)+(++d)+(++d)+(++c));

		//QUS8
		int p = 3;
		int q = 4;
		System.out.println((++p)+(--p)+(++q)+(--q)+(--p)+(++q));

		//QUS9
		int s = 13;
		int t = 14;
		System.out.println((++s)+(++z)+(++t)+(++t)+(++t));

		//QUS10
		int p1 = 11;
		System.out.println((--p1)+(--p1)+(--p1)+(--p1)+(--p1)+(--p1));
	}
}