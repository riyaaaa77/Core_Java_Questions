class Increment
{
	public static void main(String [] args)
	{
		//QUS1
		int p = 3;
		System.out.println(++p);

		//QUS2
		int q = 4;
		System.out.println(++q);

		//QUS3
		int x = 150;
		System.out.println(++x);

		//QUS4
		int z = -3;
		System.out.println(++z);

		//QUS5
		int y = -7;
		System.out.println(++y);
	}
}























