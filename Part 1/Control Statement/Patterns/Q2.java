class Q2
{
	public static void main(String [] args)
	{
		//for loop

		for(int i = 1;i<=2;i++)
		{
			for(char c = 'C'; c <='F'; c++)
			{	
				System.out.print(c);
			}
		System.out.println();
		}
		
		//While loop

		int i1 = 1;
		while(i1<=2)
		{
			char c1 = 'C';
			while(c1<='F')
			{
				System.out.print(c1);
				c1++;
			}
		i1++;
		System.out.println();
		}
		
		//do while loop

	}

}